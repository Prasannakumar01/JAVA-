package basic_programming;

public class SwapusingThirdVariable {

	public static void main(String[] args) {
		int a=1;
		int b=2;
		int c;
		
		System.out.println("Before Swapping:"+a+"  "+b);
		 c=a;
		 a=b;
		 b=c;
		 
		 System.out.println("After Swapping:"+a+"  "+b);
		
	}

}
